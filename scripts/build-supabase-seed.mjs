import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const atlasData = JSON.parse(readFileSync(join(root, "src", "data", "generated", "atlas-data.json"), "utf8"));
const sourceData = JSON.parse(readFileSync(join(root, "src", "data", "generated", "sources.json"), "utf8"));

const outputPath = join(root, "supabase", "seed.sql");

const plainSummaries = {
  "arctic-isr-drones":
    "Find public evidence of Canadian drone, sensor, satellite, and Arctic monitoring capacity.",
  "secure-communications":
    "Find companies, contracts, and research signals connected to secure networks, cyber tools, cloud, chips, and command systems.",
  "naval-autonomy":
    "Find public evidence for autonomous ships, underwater systems, naval sensors, and marine engineering.",
};

function sqlString(value) {
  if (value === null || value === undefined) return "null";
  return `'${String(value).replaceAll("'", "''")}'`;
}

function sqlNumber(value) {
  if (value === null || value === undefined || Number.isNaN(Number(value))) return "null";
  return String(Number(value));
}

function sqlTimestamp(value) {
  return `${sqlString(value)}::timestamptz`;
}

function sqlJson(value) {
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}

function sqlTextArray(values) {
  if (!Array.isArray(values) || values.length === 0) return "array[]::text[]";
  return `array[${values.map((value) => sqlString(value)).join(", ")}]::text[]`;
}

function documentTypeForSource(sourceType) {
  if (sourceType.includes("csv") || sourceType.includes("dataset") || sourceType.includes("geodata")) return "dataset";
  return "source_page";
}

const chunks = [
  "-- Generated by scripts/build-supabase-seed.mjs. Do not hand-edit data rows here.",
  "begin;",
];

for (const source of sourceData.sources) {
  chunks.push(`
insert into public.sources (id, title, publisher, url, source_type, tier, cadence, use_text)
values (${sqlString(source.id)}, ${sqlString(source.title)}, ${sqlString(source.publisher)}, ${sqlString(source.url)}, ${sqlString(source.sourceType)}, ${sqlNumber(source.tier)}, ${sqlString(source.cadence)}, ${sqlString(source.use)})
on conflict (id) do update set
  title = excluded.title,
  publisher = excluded.publisher,
  url = excluded.url,
  source_type = excluded.source_type,
  tier = excluded.tier,
  cadence = excluded.cadence,
  use_text = excluded.use_text,
  updated_at = now();

insert into public.documents (source_id, title, url, publisher, document_type, summary, content_hash, status, is_public, metadata)
values (${sqlString(source.id)}, ${sqlString(source.title)}, ${sqlString(source.url)}, ${sqlString(source.publisher)}, ${sqlString(documentTypeForSource(source.sourceType))}, ${sqlString(source.use)}, ${sqlString(source.id)}, 'published', true, ${sqlJson({ seededFrom: "source_catalogue", generatedAt: sourceData.generatedAt })})
on conflict (url) do update set
  source_id = excluded.source_id,
  title = excluded.title,
  publisher = excluded.publisher,
  document_type = excluded.document_type,
  summary = excluded.summary,
  content_hash = excluded.content_hash,
  status = excluded.status,
  is_public = excluded.is_public,
  metadata = excluded.metadata,
  updated_at = now();`);
}

for (const region of atlasData.regions) {
  chunks.push(`
insert into public.regions (id, name, short_name, dguid)
values (${sqlString(region.id)}, ${sqlString(region.name)}, ${sqlString(region.shortName)}, ${sqlString(region.dguid)})
on conflict (id) do update set
  name = excluded.name,
  short_name = excluded.short_name,
  dguid = excluded.dguid,
  updated_at = now();`);
}

for (const mission of atlasData.missions) {
  chunks.push(`
insert into public.capability_areas (
  id, name, short_name, description, plain_language_summary, source_ids, taxonomy, weights, national_signals, national_metrics, is_public
)
values (
  ${sqlString(mission.id)},
  ${sqlString(mission.name)},
  ${sqlString(mission.shortName)},
  ${sqlString(mission.description)},
  ${sqlString(plainSummaries[mission.id] ?? mission.description)},
  ${sqlTextArray(mission.sourceIds)},
  ${sqlJson(mission.taxonomy)},
  ${sqlJson(mission.weights)},
  ${sqlJson(mission.nationalSignals)},
  ${sqlJson(mission.nationalMetrics)},
  true
)
on conflict (id) do update set
  name = excluded.name,
  short_name = excluded.short_name,
  description = excluded.description,
  plain_language_summary = excluded.plain_language_summary,
  source_ids = excluded.source_ids,
  taxonomy = excluded.taxonomy,
  weights = excluded.weights,
  national_signals = excluded.national_signals,
  national_metrics = excluded.national_metrics,
  is_public = excluded.is_public,
  updated_at = now();`);
}

for (const score of atlasData.scores) {
  chunks.push(`
insert into public.region_scores (
  capability_area_id, region_id, strength_score, confidence, source_ids, indicators, generated_at
)
values (
  ${sqlString(score.missionId)},
  ${sqlString(score.regionId)},
  ${sqlNumber(score.readinessScore)},
  ${sqlString(score.confidence)},
  ${sqlTextArray(score.sourceIds)},
  ${sqlJson(score.indicators)},
  ${sqlTimestamp(atlasData.generatedAt)}
)
on conflict (capability_area_id, region_id) do update set
  strength_score = excluded.strength_score,
  confidence = excluded.confidence,
  source_ids = excluded.source_ids,
  indicators = excluded.indicators,
  generated_at = excluded.generated_at,
  updated_at = now();`);
}

chunks.push(`
insert into atlas_private.source_runs (run_type, status, started_at, finished_at, records_seen, records_changed, metadata)
values (
  'seed',
  'succeeded',
  ${sqlTimestamp(atlasData.generatedAt)},
  now(),
  ${sqlNumber(atlasData.scores.length + sourceData.sources.length + atlasData.regions.length + atlasData.missions.length)},
  ${sqlNumber(atlasData.scores.length + sourceData.sources.length + atlasData.regions.length + atlasData.missions.length)},
  ${sqlJson({ generatedAt: atlasData.generatedAt, artifactVersion: atlasData.methodology.version })}
);

commit;
`);

mkdirSync(join(root, "supabase"), { recursive: true });
writeFileSync(outputPath, `${chunks.join("\n")}\n`);

console.log(`Wrote ${outputPath}`);
